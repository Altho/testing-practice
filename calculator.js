const calculator = {
    add: function(number1,number2){
        return number1+number2;
    },
    substract: function(number1,number2){
        return number1-number2;
    },
    multiply: function(number1,number2){
        return number1*number2;
    },
    divide:function(number1,number2){
        return number1/number2;
}}

module.exports=calculator;